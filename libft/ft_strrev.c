#include "libft.h"

char		*ft_strrev(char *str)
{
	int		l;
	int		i;
	char	c;

	l = 0;
	i = 0;
	while (str[l] != '\0')
		l++;
	l--;
	while (i < ((l + 1) / 2))
	{
		c = str[i];
		str[i] = str[l - i];
		str[l - i] = c;
		i++;
	}
	return (str);
}
