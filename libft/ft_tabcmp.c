int			ft_tabcmp(char **tab1, char **tab2)
{
	int		i;
	int		j;

	i = -1;
	while (tab1[++i])
	{
		j = -1;
		while (tab1[i][++j])
			if (tab1[i][j] != tab2[i][j])
				return (0);
	}
	return (1);
}
