void		ft_cpytabat(char **tab, char **tab2, int i, int j)
{
	int		k;
	int		l;

	k = -1;
	while (tab[++k])
	{
		l = -1;
		while (tab[k][++l])
			if (tab[k][l] != '.')
				tab2[i + k][j + l] = tab[k][l];
	}
}
