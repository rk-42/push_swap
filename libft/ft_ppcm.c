#include "libft.h"

int			ft_ppcm(int a, int b)
{
	int		x;
	int		y;

	x = a;
	y = b;
	while (x != y)
	{
		while (x > y)
			y = y + b;
		while (x < y)
			x = x + a;
	}
	return (x);
}
